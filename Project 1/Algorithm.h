/// MAXIMUM
/// Returns the maximum of the two values.
#ifndef MAXIMUM
#define MAXIMUM(A,B) ( ((A) > (B)) ? (A) : (B) )
#endif

/// MINIMUM
/// Returns the minimum of the two values.
#ifndef MINIMUM
#define MINIMUM(A,B) ( ((A) < (B)) ? (A) : (B) )
#endif

#endif

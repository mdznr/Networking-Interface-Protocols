#ifndef Project_1_Algorithm_h
#define Project_1_Algorithm_h

/// MAX
/// Returns the maximum of the two values.
#ifndef MAX
#define MAX(A,B) ( ((A) > (B)) ? (A) : (B) )
#endif

/// MIN
/// Returns the minimum of the two values.
#ifndef MIN
#define MIN(A,B) ( ((A) < (B)) ? (A) : (B) )
#endif

#endif
